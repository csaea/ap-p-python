#KEY CONCEPTS: print statements, data types, strings, integers, floats, booleans

# The following are common (but not all) data types for python. 
#These are the only ones you'll need to know for AP CSP exam

#STRINGS
print("DATA TYPE: STRINGS\n") 
#Regular language, in ASCII characters including number, contained by quotations marks
print("Hello world")
print("-----")
print("Once upon a time and a very good time it was there was a moocow coming down along the road and this moocow that was down along the road met a nicens little boy named baby tuckoo")
print(" ")
print("Once upon a time \nand a very good time it was \nthere was a moocow\n")
print("Name: \tBilly \nAge: \t14 \nGrade: \t9")

#INTEGER 
print("\nDATA TYPE: INTEGERS\n") 
#whole numbers, positive and negative
print(42)
print(-23)
print(5 + 5 - 9)

#FLOAT
print("\nDATA TYPE: FLOATS\n") 
#decimal point numbers.
print(4.0)
print(2.0 + 4.5)
print(2.0 + 4)

#BOOLEAN
print("\nDATA TYPE: BOOLEANS\n") 
#True or false statements
print(True)
print(False)
print(1 == 1)
print(1 != 1)
print(5 + 5 == 11)
print("Hello" == "Hello")

#How do I know what data type I'm working with? 
print("\nDATA TYPE LOOKUP:\n") 
type("To see this message, wrap me in a print statement")
print(type("Hello world"))
print(type(3))
print(type(3.14))
print(type(True))

print("\n") 
