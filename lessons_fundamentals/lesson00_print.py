# KEY CONCEPTS: print statements, comments, running code.

# Write comments after the hashtag(#). Python won't run these lines. They are for your code comments while you develop. 
# It is common to delete all comments before you deploy (release) your code to the public. 

# The print command print() will display its contents in the system Terminal (found in the bottom window of VSCode). Typically used for debugging your program.  

print("Hello world")
print(42)

# Print can take in any number of arguments, separated by a comma. This helps for clarity in the output. 
print("Age:", 24, "Numbers:", 1, 2, 3)

# Print can also run expressions and methods: 
print("Sum: ", 4 + 5)